    private void loadData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Asal");
        model.addColumn("Tujuan");
        model.addColumn("No Duduk");
        model.addColumn("Keberangkatan");
        model.addColumn("Harga");
        tblResult.setModel(model);

        //menampilkan data database kedalam tabel
        try {
            java.sql.Statement stat = koneksi.createStatement();
            ResultSet data = stat.executeQuery("SELECT * FROM ticket INNER JOIN destination ON destination.id=ticket.destination_id");
        while (data.next()) {
            model.addRow(new Object[]{
            data.getString("id"),
            data.getString("name"),
            data.getString("tbl_from"),
            data.getString("tbl_to"),
            data.getString("no_seat"),
            data.getString("departure"),
            data.getString("price"),
         });
         tblResult.setModel(model);
        }
        } catch (Exception e) {
        System.err.println("Terjadi Kesalahan :" + e);
        }
    }
    
