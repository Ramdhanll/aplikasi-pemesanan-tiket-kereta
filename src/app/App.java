    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String from = (String) cbAsal.getSelectedItem();
        String to = (String) cbTujuan.getSelectedItem();
        String destination_id =lblIDDestination.getText();
        String price = txtPrice.getText();
        String name = txtName.getText();
        String no_seat = txtNoSeat.getText();
        int id_ticket = (int) (Math.random() * 1000);
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MMMM-yyyy");
        String date = sdf.format(txtDeparture.getDate());
        
        // Validasi Nama
        if(name.equals("")){
            JOptionPane.showMessageDialog(null, "Input field nama", "Failed", 
                                    JOptionPane.INFORMATION_MESSAGE);
            
            return;
        }
        
        // Validasi no seat
        if(no_seat.equals("")){
            JOptionPane.showMessageDialog(null, "Pilih tempat duduk", "Failed", 
                                    JOptionPane.INFORMATION_MESSAGE);
            
            return;
        }
        
        // Validasi Kembalian
        if(txtBayar.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Input field di bayar !", "Failed", 
                                    JOptionPane.INFORMATION_MESSAGE);
            
            return;
        }
        
        int kembalian = Integer.parseInt(txtKembalian.getText());
        if(kembalian < 0) {
            JOptionPane.showMessageDialog(null, "Pembayaran belum memenuhi syarat", "Failed", 
                                    JOptionPane.INFORMATION_MESSAGE);
            
            return;
        } else if (txtKembalian.getText() == null){
            JOptionPane.showMessageDialog(null, "Pembayaran belum memenuhi syaratf", "Failed", 
                                    JOptionPane.INFORMATION_MESSAGE);
            
            return;
        }
    
         if (txtName.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Mohon Masukkan Data untuk di Input", "PERHATIAN", 
                        JOptionPane.WARNING_MESSAGE);
                } else {
                try {
                    try (java.sql.Statement stat = koneksi.createStatement()) {
                        ResultSet data = stat.executeQuery("SELECT * FROM ticket WHERE name ='" + 
                                txtName.getText() + "'");
                        if (data.next()) {
                            JOptionPane.showMessageDialog(null, "Data anda sudah terdaftar", "PERHATIAN", 
                                    JOptionPane.WARNING_MESSAGE);
                            txtName.requestFocus();
                        } else {
                            String sql = "INSERT INTO ticket VALUES("  + id_ticket + ""
                                    + ",'" + destination_id + "'"
                                    + ",'" + name + "'"
                                    + ",'" + no_seat + "'"
                                    + ",'" + date + "')";
                            
                            stat.executeUpdate(sql);
                            reset();
                            JOptionPane.showMessageDialog(null, "Data Berhasil Di Simpan", "SUKSES", 
                                    JOptionPane.INFORMATION_MESSAGE);
                        }   }
                } catch (HeadlessException | SQLException exc) {
                    System.err.println("Terjadi Kesalahan :" + exc);
                }
            }
            loadData();
        
    }//GEN-LAST:event_btnSimpanActionPerformed

